// Celsius a Fahrenheit

// Crear un programa que pida ingresar una 
// cantidad de grados Celsius y muestre un mensaje con el resultado de la conversión a grados Fahrenheit.

const celsius = prompt('Ingrese una cantidad en grados celsius');
const resultados = (celsius * 1.8) + 32;
alert(`${celsius}° celsius son ${resultados}°F`);
