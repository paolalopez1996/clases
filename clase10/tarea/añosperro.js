// Años perro

// Crear un programa que pida ingresar una edad y devuelva el equivalente de esas edad en años perro.

const edad = prompt("¿qué edad tienes?");
const añosPerro = 7;
const resultados = edad * añosPerro;

alert("tu edad en años perro es" +  resultados); //concatenacion
alert(`tu edad en años perros es ${resultados}!!`); //template string