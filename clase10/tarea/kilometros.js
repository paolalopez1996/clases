// Kilómetros a millas

// Crear un programa que pida ingresar una cantidad de kilómetros y muestre un mensaje con el resultado de la conversión en millas.


const kilometros = prompt('ingrese una cantidad en kilómetros para saber su consersión en millas')
const milla = 1609;
const resultado = kilometros * milla;

alert(`${kilometros} son ${resultado} millas`)